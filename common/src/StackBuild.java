import java.util.ArrayList;
import java.util.List;
import java.util.Stack;

public class StackBuild {

    public static void main(String[] args) {
        Stack<Integer> stack = new Stack<>();
        List<Integer> list = new ArrayList<>();
        list.add(10);
        list.get(0);

        stack.isEmpty();
        stack.add(1);
        stack.peek();
        stack.pop();

    }
}
